import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const iconDir = path.join(root, ".assets", "icon");
const outFile = path.join(root, "src", "components", "dv-assets", "board", "board-preset-icon-markups.generated.ts");

function collectIds(svg) {
  const ids = new Set();
  const re = /\bid="([^"]+)"/g;
  let m;
  while ((m = re.exec(svg))) ids.add(m[1]);
  return [...ids].sort((a, b) => b.length - a.length);
}

function prefixSvgIds(svg, token) {
  let out = svg;
  for (const id of collectIds(svg)) {
    const prefixed = `${token}-${id}`;
    out = out.split(`id="${id}"`).join(`id="${prefixed}"`);
    out = out.split(`url(#${id})`).join(`url(#${prefixed})`);
    out = out.split(`xlink:href="#${id}"`).join(`xlink:href="#${prefixed}"`);
    out = out.split(`href="#${id}"`).join(`href="#${prefixed}"`);
  }
  return out;
}

const TOKEN = "__IDPFX__";

const header = `/**
 * Auto-generated by scripts/gen-board-preset-icon-markups.mjs from .assets/icon/1.svg … 6.svg.
 * Do not edit by hand; re-run the script after changing source SVGs.
 */
import type { SvgMarkupBuilderArgs } from "@/lib/rasterized-svg/svg-markup-types";

`;

const parts = [header];

for (let i = 1; i <= 6; i++) {
  const svgPath = path.join(iconDir, `${i}.svg`);
  const raw = fs.readFileSync(svgPath, "utf8");
  const prefixed = prefixSvgIds(raw, TOKEN);
  const json = JSON.stringify(prefixed);
  parts.push(`const RAW_PRESET_ICON_${i} = ${json};\n\n`);
  parts.push(
    `export function buildBoardPresetIcon${i}SvgMarkup({ idPrefix, color: _c }: SvgMarkupBuilderArgs): string {\n` +
      `  void _c;\n` +
      `  return (JSON.parse(RAW_PRESET_ICON_${i}) as string).split(${JSON.stringify(TOKEN)}).join(idPrefix);\n` +
      `}\n\n`
  );
}

fs.writeFileSync(outFile, parts.join(""), "utf8");
console.log("Wrote", outFile);
